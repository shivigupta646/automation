package progrmpractices;

public class UsingThirdVarSwap {

	public static void main(String[] args) {
		  
		int a, b , t;
		a = 10;
		b = 20;
		 System.out.println("\nbefore Swapping :" );
		 System.out.println( a + "  " + b );
		 
		 t = a;
		 a = b;
		 b = t;
		   
		 
		 System.out.println("\nAfter Swapping : " );
		 System.out.println( a + "  " + b );
	}
       
}
